import type { Metadata } from "next";
import { Inter as FontSans } from "next/font/google";
import "./globals.css";
import "./envConfig";
import { FC } from "react";
import { cn } from "@/lib/utils";
import { Navbar } from "@/components/Navbar";
import { Footer } from "@/components/Footer";
import { ThemeProvider } from "next-themes";
import { CategoryText } from "@/components/CategoryText";
import { Toaster } from "@/components/ui/toaster";
import { Sell_btn } from "@/components/Sell_btn";
import { LayoutProps } from "@/interfaces";
import connect_to_database from "@/lib/mongoose";
import StoreProvider from "@/app/StoreProvider";
import axios from "axios";
import { cookies } from "next/headers";
import { Loader } from "@/components/Loader";

await connect_to_database();

const fontSans = FontSans({
  subsets: ["cyrillic-ext"],
  variable: "--font-sans",
});

export const metadata: Metadata = {
  title: "LOX - Buy & Sell Products anywhere in Pakistan",
  description: "Generated by create next app",
  keywords: "LOX, Buy, Sell, Products, Pakistan",
};

const RootLayout: FC<LayoutProps> = async ({ children }) => {
  const getToken = await (
    await cookies()
  ).get(process.env.AUTH_TOKEN_KEY || "");

  const authApiHandle = async () => {
    try {
      const res = await axios.post(
        `${process.env.BACKEND_URL}/api/authCheck`,
        getToken?.value
      );
      console.log("res.data", res.data);
      return res.data;
    } catch (err) {
      console.log("err", err);
    }
  };

  return (
    <html lang="en" suppressHydrationWarning>
      <body
        id="top"
        className={cn(
          "min-h-screen bg-background text-foreground font-sans antialiased xl:container flex flex-col justify-between",
          fontSans.variable
        )}
      >
        <ThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
          disableTransitionOnChange
        >
          <StoreProvider authApiResponse={await authApiHandle()}>
            <div>
              <Sell_btn className="fixed bottom-3 left-1/2 transform -translate-x-1/2 xmd:hidden z-50" />
              <Navbar />
              <CategoryText />
              {children}
            </div>
            <div>
              <Footer />
            </div>
            <Loader />
          </StoreProvider>
        </ThemeProvider>
        <Toaster />
      </body>
    </html>
  );
};
export default RootLayout;
